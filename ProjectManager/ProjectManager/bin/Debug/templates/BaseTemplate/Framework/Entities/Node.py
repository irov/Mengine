#coding=UTF-8
import Menge


# ------------------------------------------------------------------------------
# Class: Node
# Description:
# - 
# ------------------------------------------------------------------------------
class Node( Menge.Entity ):
	# ----------------------------------------------------------------------------
	# Method: __init__
	# Description:
	# - 
	# ----------------------------------------------------------------------------
	def __init__( self ):
		Menge.Entity.__init__( self )
		
		self.node = None
		
		self.cbMoveEnd = None
		pass
	
	# ----------------------------------------------------------------------------
	# Method: onLoader
	# Description:
	# - 
	# ----------------------------------------------------------------------------
	def onLoader( self ):
		self.node = self.getChildren( "Node", True )
		
		pass
	
	# ----------------------------------------------------------------------------
	# Method: functor
	# Description:
	# - 
	# ----------------------------------------------------------------------------
	def functor( self, func ):
		func( self.node )
		pass
	
	# ----------------------------------------------------------------------------
	# Method: setCbMoveEnd
	# Description:
	# - 
	# ----------------------------------------------------------------------------
	def setCbMoveEnd( self, fn ):
		self.cbMoveEnd = fn
		pass
	
	# ----------------------------------------------------------------------------
	# Method: onMoveEnd
	# Description:
	# - 
	# ----------------------------------------------------------------------------
	def onMoveEnd( self ):
		if self.cbMoveEnd != None:
			self.cbMoveEnd()
		pass
	
	# ----------------------------------------------------------------------------
	# Method: onMoveEnd
	# Description:
	# - 
	# ----------------------------------------------------------------------------
	def getNode( self ):
		return self.node