#	include "Factory.h"
#	include "Factorable.h"

namespace Menge
{
	//////////////////////////////////////////////////////////////////////////
	Factory::Factory()
	{
	}
	//////////////////////////////////////////////////////////////////////////
	Factory::~Factory()
	{
	}
	//////////////////////////////////////////////////////////////////////////
	Factorable * Factory::createObject( const ConstString & _type )
	{
		Factorable * object = this->_createObject( _type );
		object->setFactory( this );
		return object;
	}
}
