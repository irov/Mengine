#	include "HTTPLogger.h"

namespace Menge
{
	//////////////////////////////////////////////////////////////////////////
	HTTPLogger::HTTPLogger( HTTPSystemInterface * _interfaceHTTP )
		: m_isValid(false)
		, m_interface( _interfaceHTTP )
	{
	}
	//////////////////////////////////////////////////////////////////////////
	HTTPLogger::~HTTPLogger()
	{
	}
	//////////////////////////////////////////////////////////////////////////
	bool HTTPLogger::isValid() const
	{
		return m_isValid;
	}
	//////////////////////////////////////////////////////////////////////////
	void  HTTPLogger::setVerboseLevel( HTTPLogger::EVerboseLevel _level )
	{
		m_verboseLevel = _level; 
	}
	//////////////////////////////////////////////////////////////////////////
	HTTPLogger::EVerboseLevel  HTTPLogger::getVerboseLevel( ) const
	{
		return m_verboseLevel;
	}
	//////////////////////////////////////////////////////////////////////////
	void HTTPLogger::_debugMessage(const char * _message) const
	{
		if( m_verboseLevel != EVerboseLevel::VL_STDERR )
		{
			return;
		}

		fprintf ( stderr , "%s \n",_message );
	}
	//////////////////////////////////////////////////////////////////////////
}

