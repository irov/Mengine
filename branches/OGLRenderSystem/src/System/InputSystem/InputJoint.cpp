#	include "InputJoint.h"
#	include "InputSystem.h"

//////////////////////////////////////////////////////////////////////////
InputJoint::InputJoint( InputSystem * _system )
: m_system(_system)
, m_active(false)
, m_pDev(0)
{
	
}
//////////////////////////////////////////////////////////////////////////
HRESULT InputJoint::acquire()
{
	if( m_pDev )
	{
		return m_pDev->Acquire();
	}
	return -1;
}
//////////////////////////////////////////////////////////////////////////
HRESULT InputJoint::unacquire()
{
	if( m_pDev )
	{
		return m_pDev->Unacquire();
	}
	return -1;
}
//////////////////////////////////////////////////////////////////////////
void InputJoint::release()
{

}
//////////////////////////////////////////////////////////////////////////
bool InputJoint::isActive() const
{
	return m_active;
}
//////////////////////////////////////////////////////////////////////////
void InputJoint::setActive( bool _value )
{
	m_active = _value;
}
//////////////////////////////////////////////////////////////////////////
LPDIRECTINPUT8 InputJoint::getDirectInput()
{
	return m_system->getDirectInput();
}
//////////////////////////////////////////////////////////////////////////
HWND InputJoint::getHWnd()
{
	return m_system->getHWnd();
}