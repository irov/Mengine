#include "Arena3DPcg32.h"

//////////////////////////////////////////////////////////////////////////
namespace Arena3D
{
    //////////////////////////////////////////////////////////////////////////
    Pcg32::Pcg32()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    Pcg32::Pcg32( uint64_t _seed, uint64_t _stream )
    {
        this->seed( _seed, _stream );
    }
    //////////////////////////////////////////////////////////////////////////
    void Pcg32::seed( uint64_t _seed, uint64_t _stream )
    {
        kf_pcg32_seed( &m_random, _seed, _stream );
    }
    //////////////////////////////////////////////////////////////////////////
    uint32_t Pcg32::next()
    {
        return kf_pcg32_next( &m_random );
    }
    //////////////////////////////////////////////////////////////////////////
    uint32_t Pcg32::bounded( uint32_t _bound )
    {
        return kf_pcg32_bounded( &m_random, _bound );
    }
    //////////////////////////////////////////////////////////////////////////
    uint64_t Pcg32::state() const
    {
        return m_random.state;
    }
    //////////////////////////////////////////////////////////////////////////
    uint64_t Pcg32::stream() const
    {
        return m_random.increment;
    }
    //////////////////////////////////////////////////////////////////////////
    void Pcg32::restore( uint64_t _state, uint64_t _stream )
    {
        kf_pcg32_restore( &m_random, _state, _stream );
    }
    //////////////////////////////////////////////////////////////////////////
}
//////////////////////////////////////////////////////////////////////////
