#	include "OgreFileData.h"

OgreFileData::OgreFileData( Ogre::DataStreamPtr _data )
: m_data( _data )
{

}
//////////////////////////////////////////////////////////////////////////
OgreFileData::~OgreFileData()
{
	m_data->close();
}
//////////////////////////////////////////////////////////////////////////
size_t OgreFileData::read( void* _buffer, size_t _count )
{
	return m_data->read( _buffer, _count );
}
//////////////////////////////////////////////////////////////////////////
bool OgreFileData::eof() const
{
	return m_data->eof();
}
//////////////////////////////////////////////////////////////////////////
size_t OgreFileData::size() const
{
	return m_data->size();
}
//////////////////////////////////////////////////////////////////////////
void OgreFileData::seek( size_t _pos )
{
	m_data->seek( _pos );
}
//////////////////////////////////////////////////////////////////////////
size_t OgreFileData::tell() const
{
	return m_data->tell();
}
//////////////////////////////////////////////////////////////////////////
std::string OgreFileData::getLine( bool _trimAfter ) const
{
	return m_data->getLine( _trimAfter );
}
//////////////////////////////////////////////////////////////////////////
size_t OgreFileData::skipLine( const std::string & _delim )
{
	return m_data->skipLine( _delim );
}