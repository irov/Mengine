#include "Kernel/Documentable.h"

namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    Documentable::Documentable()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    Documentable::~Documentable()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    void Documentable::setDocument( const Char * _doc )
    {
        m_document = _doc;
    }
    //////////////////////////////////////////////////////////////////////////
    const Char * Documentable::getDocument() const
    {
        const Char * doc = m_document.c_str();

        return doc;
    }
}
