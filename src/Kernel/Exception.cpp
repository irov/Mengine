#include "Exception.h"

//////////////////////////////////////////////////////////////////////////
namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    Exception::Exception( const Char * _message ) noexcept
    {
        m_message.assign( _message );
    }
    //////////////////////////////////////////////////////////////////////////
    Exception::~Exception() noexcept
    {
    }
    //////////////////////////////////////////////////////////////////////////
    const char * Exception::what() const noexcept
    {
        return m_message.c_str();
    }
    //////////////////////////////////////////////////////////////////////////
}

