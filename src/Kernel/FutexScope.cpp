#include "FutexScope.h"

namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    FutexScope::FutexScope( Futex & _futex )
        : m_futex( _futex )
    {
        m_futex.lock();
    }
    //////////////////////////////////////////////////////////////////////////
    FutexScope::~FutexScope()
    {
        m_futex.unlock();
    }
    //////////////////////////////////////////////////////////////////////////
}