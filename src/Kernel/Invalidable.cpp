#include "Invalidable.h"

namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    Invalidable::Invalidable()
        : m_invalidateFlags( ~0U )
    {
    }
    //////////////////////////////////////////////////////////////////////////
    Invalidable::~Invalidable()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    void Invalidable::validate( uint32_t _flags ) const
    {
        m_invalidateFlags &= ~_flags;
    }
    //////////////////////////////////////////////////////////////////////////
    void Invalidable::invalidate( uint32_t _flags ) const
    {
        m_invalidateFlags |= _flags;
    }
    //////////////////////////////////////////////////////////////////////////
}
