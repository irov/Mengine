#include "SpinLockScope.h"

namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    SpinLockScope::SpinLockScope( SpinLock & _futex )
        : m_spinlock( _futex )
    {
        m_spinlock.lock();
    }
    //////////////////////////////////////////////////////////////////////////
    SpinLockScope::~SpinLockScope()
    {
        m_spinlock.unlock();
    }
    //////////////////////////////////////////////////////////////////////////
}