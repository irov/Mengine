#include "ThreadGuard.h"

#include "Kernel/Crash.h"

namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    ThreadGuard::ThreadGuard()
        : m_lock( false )
    {
    }
    //////////////////////////////////////////////////////////////////////////
    ThreadGuard::~ThreadGuard()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    bool ThreadGuard::lock( bool _value ) const
    {
        bool lock = m_lock;

        m_lock = _value;

        return lock;
    }
    //////////////////////////////////////////////////////////////////////////
}