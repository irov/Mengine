#	include "ActionLook.h"	

#	include "Avatar3D.h"	

namespace Menge
{ 
	//////////////////////////////////////////////////////////////////////////
	ActionLook::ActionLook(Avatar3D * _avatar, const mt::vec3f& _destPoint)
		: Action()
		, m_avatar(_avatar)
		, m_destPoint(_destPoint)
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void	ActionLook::update( float _timing )
	{
		if(m_avatar->isMoving() == false)
		{
			m_isEnded = true;
		}
	}
	//////////////////////////////////////////////////////////////////////////
	void	ActionLook::run()
	{
		m_avatar->lookTo(m_destPoint);
	}
	//////////////////////////////////////////////////////////////////////////
	void	ActionLook::end()
	{}
	//////////////////////////////////////////////////////////////////////////
}