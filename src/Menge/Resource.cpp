#	include "Resource.h"

namespace Menge
{
	//////////////////////////////////////////////////////////////////////////
	Resource::Resource()
		: m_compile(false)
	{}
	//////////////////////////////////////////////////////////////////////////
	bool Resource::compile()
	{
		if( m_compile )
		{
			return true;
		}

		m_compile = _compile();

		return m_compile;
	}
	//////////////////////////////////////////////////////////////////////////
	void Resource::release()
	{
		if( m_compile )
		{
			m_compile = false;
			_release();
		}
	}
	//////////////////////////////////////////////////////////////////////////
	bool Resource::isCompile() const
	{
		return m_compile;
	}
	//////////////////////////////////////////////////////////////////////////
	bool Resource::_compile()
	{
		return true;
	}
	//////////////////////////////////////////////////////////////////////////
	void Resource::_release()
	{
		//Empty
	}
}