#	include "ScriptObject.h"

namespace Menge
{
	//////////////////////////////////////////////////////////////////////////
	ScriptObject::ScriptObject()
	{
	}
	//////////////////////////////////////////////////////////////////////////
	ScriptObject::ScriptObject( const boost::python::object & obj )
		: boost::python::object( obj )
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void ScriptObject::incref()
	{
		boost::python::incref( ptr() );
	}
	//////////////////////////////////////////////////////////////////////////
	void ScriptObject::decref()
	{
		boost::python::decref( ptr() );
	}
}