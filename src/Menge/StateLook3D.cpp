#	include "StateLook3D.h"	

#	include "Avatar3D.h"	

namespace Menge
{ 
	//////////////////////////////////////////////////////////////////////////
	StateLook3D::StateLook3D( Avatar3D * _avatar, const mt::vec3f& _destPoint )
		: m_avatar( _avatar )
		, m_destPoint( _destPoint )
		, m_isEnded( false )
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void	StateLook3D::update( float _timing )
	{
		if( m_avatar->isMoving() == false )
		{
			m_isEnded = true;
		}
	}
	//////////////////////////////////////////////////////////////////////////
	void	StateLook3D::run()
	{
		printf("StateLook3D is running! \n");
		m_avatar->lookTo(m_destPoint);
	}
	//////////////////////////////////////////////////////////////////////////
	void	StateLook3D::end()
	{
		printf("StateLook3D is ended! \n");
	}
	//////////////////////////////////////////////////////////////////////////
	bool	StateLook3D::isEnded() const
	{
		return m_isEnded;
	}
	//////////////////////////////////////////////////////////////////////////
}