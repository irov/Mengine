#	include "StateMove3D.h"	

#	include "Avatar3D.h"	

namespace Menge
{ 
	//////////////////////////////////////////////////////////////////////////
	StateMove3D::StateMove3D(Avatar3D * _avatar, const mt::vec3f& _destPoint)
		: m_avatar( _avatar ) 
		, m_destPoint( _destPoint )
		, m_isEnded( false )
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void	StateMove3D::update( float _timing )
	{
		if(m_avatar->isMoving() == false)
		{
			m_isEnded = true;
		}
	}
	//////////////////////////////////////////////////////////////////////////
	void	StateMove3D::run()
	{
		printf("StateMove3D is running! \n");
		m_avatar->moveTo(m_destPoint);
	}
	//////////////////////////////////////////////////////////////////////////
	void	StateMove3D::end()
	{
		printf("StateMove3D is ended! \n");
		m_avatar->stop();
	}
	//////////////////////////////////////////////////////////////////////////
	bool	StateMove3D::isEnded() const
	{
		return m_isEnded;
	}
	//////////////////////////////////////////////////////////////////////////
}