#include "AOIActor.h"

namespace Mengine
{
	//////////////////////////////////////////////////////////////////////////
	AOIActor::AOIActor()
		: m_provider( nullptr )
		, m_remove(false)
	{
	}
	//////////////////////////////////////////////////////////////////////////
	AOIActor::~AOIActor()
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void AOIActor::setProvider( const AOIActorProviderInterfacePtr & _provider )
	{
		m_provider = _provider;
	}
	//////////////////////////////////////////////////////////////////////////
	const AOIActorProviderInterfacePtr & AOIActor::getProvider()
	{
		return m_provider;
	}
	//////////////////////////////////////////////////////////////////////////
	void AOIActor::remove()
	{
		m_remove = true;
	}
	//////////////////////////////////////////////////////////////////////////
	bool AOIActor::isRemoved() const
	{
		return m_remove;
	}
}