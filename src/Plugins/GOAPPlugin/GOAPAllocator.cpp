#include "GOAPAllocator.h"

#include "Kernel/AllocatorHelper.h"

namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    GOAPAllocator::GOAPAllocator()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    GOAPAllocator::~GOAPAllocator()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    void * GOAPAllocator::allocate( size_t _size )
    {
        void * p = Helper::allocateMemory( _size, "goap" );

        return p;
    }
    //////////////////////////////////////////////////////////////////////////
    void GOAPAllocator::deallocate( void * _ptr )
    {
        Helper::deallocateMemory( _ptr, "goap" );
    }
    //////////////////////////////////////////////////////////////////////////
}