#include "JSONStorage.h"

namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    JSONStorage::JSONStorage()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    JSONStorage::~JSONStorage()
    {
    }    
    //////////////////////////////////////////////////////////////////////////
    void JSONStorage::setJSON( const jpp::object & _json )
    {
        m_json = _json;
    }
    //////////////////////////////////////////////////////////////////////////
    void JSONStorage::mergeJSON( const jpp::object & _json )
    {
        jpp::merge( m_json, _json, jpp::merge_mode_e::update );
    }
}