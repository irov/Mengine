#include "LayoutEditorLogger.h"

#include "Kernel/LoggerHelper.h"
#include "Kernel/AssertionMemoryPanic.h"

namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    LayoutEditorLogger::LayoutEditorLogger()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    LayoutEditorLogger::~LayoutEditorLogger()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    const Vector<LoggerRecordInterfacePtr> & LayoutEditorLogger::getRecords() const
    {
        return m_records;
    }
    //////////////////////////////////////////////////////////////////////////
    void LayoutEditorLogger::clearRecords()
    {
        m_records.clear();
    }
    //////////////////////////////////////////////////////////////////////////
    bool LayoutEditorLogger::_initializeLogger()
    {
        return true;
    }
    //////////////////////////////////////////////////////////////////////////
    void LayoutEditorLogger::_finalizeLogger()
    {
        m_records.clear();
    }
    //////////////////////////////////////////////////////////////////////////
    void LayoutEditorLogger::_log( const LoggerRecordInterfacePtr & _record )
    {
        m_records.emplace_back( _record );
    }
    //////////////////////////////////////////////////////////////////////////
    void LayoutEditorLogger::_flush()
    {
        // Nothing to do
    }
    //////////////////////////////////////////////////////////////////////////
}

