#include "BounceActor.h"

namespace Mengine
{
	//////////////////////////////////////////////////////////////////////////
	BounceActor::BounceActor()
		: m_radius(0.f)
		, m_mass(1.f)
		, m_node( nullptr )
		, m_remove(false)
	{
	}
	//////////////////////////////////////////////////////////////////////////
	BounceActor::~BounceActor()
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void BounceActor::setRadius( float _radius )
	{
		m_radius = _radius;
	}
	//////////////////////////////////////////////////////////////////////////
	float BounceActor::getRadius() const
	{
		return m_radius;
	}
	//////////////////////////////////////////////////////////////////////////
	void BounceActor::setMass( float _mass )
	{
		m_mass = _mass;
	}
	//////////////////////////////////////////////////////////////////////////
	float BounceActor::getMass() const
	{
		return m_mass;
	}
	//////////////////////////////////////////////////////////////////////////
	void BounceActor::setNode( Node * _node )
	{
		m_node = _node;
	}
	//////////////////////////////////////////////////////////////////////////
	Node * BounceActor::getNode() const
	{
		return m_node;
	}
	//////////////////////////////////////////////////////////////////////////
	void BounceActor::remove()
	{
		m_remove = true;
	}
	//////////////////////////////////////////////////////////////////////////
	bool BounceActor::isRemoved() const
	{
		return m_remove;
	}
}