#include "PosixSocketPlugin.h"

#include "Interface/SocketSystemInterface.h"

//////////////////////////////////////////////////////////////////////////
SERVICE_EXTERN( SocketSystem );
//////////////////////////////////////////////////////////////////////////
PLUGIN_FACTORY( PosixSocket, Mengine::PosixSocketPlugin );
//////////////////////////////////////////////////////////////////////////
namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    PosixSocketPlugin::PosixSocketPlugin()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    PosixSocketPlugin::~PosixSocketPlugin()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    bool PosixSocketPlugin::_initializePlugin()
    {
        SERVICE_CREATE( SocketSystem );

        return true;
    }
    //////////////////////////////////////////////////////////////////////////
    void PosixSocketPlugin::_finalizePlugin()
    {
        SERVICE_FINALIZE( SocketSystem );
    }
    //////////////////////////////////////////////////////////////////////////
    void PosixSocketPlugin::_destroyPlugin()
    {
        SERVICE_DESTROY( SocketSystem );
    }
    //////////////////////////////////////////////////////////////////////////
}