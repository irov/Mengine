#include "MemoryProxy.h"

namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    MemoryProxy::MemoryProxy()
        : m_ptr( nullptr )
        , m_size( 0 )
    {
    }
    //////////////////////////////////////////////////////////////////////////
    MemoryProxy::~MemoryProxy()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    void MemoryProxy::setBuffer( void * _ptr, size_t _size )
    {
        m_ptr = _ptr;
        m_size = _size;
    }
    //////////////////////////////////////////////////////////////////////////
    Pointer MemoryProxy::getBuffer() const
    {
        return m_ptr;
    }
    //////////////////////////////////////////////////////////////////////////
    size_t MemoryProxy::getSize() const
    {
        return m_size;
    }
    //////////////////////////////////////////////////////////////////////////
    bool MemoryProxy::empty() const
    {
        return false;
    }
    //////////////////////////////////////////////////////////////////////////
}