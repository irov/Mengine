#	include "d3d9RenderFont.h"

#	include <map>
#	include <assert.h>
//////////////////////////////////////////////////////////////////////////
D3D9Font::D3D9Font(RenderImageInterface * _image, const FontDesc& _fontDesc)
	: m_image(_image)
{
	std::copy(
		_fontDesc.chars,
		_fontDesc.chars+256,
		stdext::checked_array_iterator<FontCharDesc*>(m_chars,256));

	m_color = _fontDesc.color;
	m_height = _fontDesc.height;
}
//////////////////////////////////////////////////////////////////////////
D3D9Font::~D3D9Font()
{}
//////////////////////////////////////////////////////////////////////////
RenderImageInterface*	D3D9Font::_get() const
{
	return m_image;
}
//////////////////////////////////////////////////////////////////////////
float D3D9Font::getHeight() const
{
	return m_height;
}
//////////////////////////////////////////////////////////////////////////
unsigned int D3D9Font::getColor() const
{
	return m_color;
}
//////////////////////////////////////////////////////////////////////////
float	D3D9Font::getCharWidth(char id) const
{
	return m_chars[id].width;
}
//////////////////////////////////////////////////////////////////////////
FontCharDesc&	D3D9Font::_getChar(char id)
{
	return m_chars[id];
}
