#include "AppleHttpResponse.h"

namespace Mengine
{
    //////////////////////////////////////////////////////////////////////////
    AppleHttpResponse::AppleHttpResponse()
        : m_requestId( 0 )
        , m_code( HTTP_OK )
        , m_successful( false )
    {
    }
    //////////////////////////////////////////////////////////////////////////
    AppleHttpResponse::~AppleHttpResponse()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    void AppleHttpResponse::setRequestId( HttpRequestId _requestId )
    {
        m_requestId = _requestId;
    }
    //////////////////////////////////////////////////////////////////////////
    HttpRequestId AppleHttpResponse::getRequestId() const
    {
        return m_requestId;
    }
    //////////////////////////////////////////////////////////////////////////
    void AppleHttpResponse::appendHeaders( const Char * _ptr, size_t _size )
    {
        m_headers.emplace_back( _ptr, _size );
    }
    //////////////////////////////////////////////////////////////////////////
    const HttpRequestHeaders & AppleHttpResponse::getHeaders() const
    {
        return m_headers;
    }
    //////////////////////////////////////////////////////////////////////////
    void AppleHttpResponse::appendData( const Char * _ptr, size_t _size )
    {
        m_data.append( _ptr, _size );
    }
    //////////////////////////////////////////////////////////////////////////
    const String & AppleHttpResponse::getData() const
    {
        return m_data;
    }
    //////////////////////////////////////////////////////////////////////////
    void AppleHttpResponse::setCode( EHttpCode _code )
    {
        m_code = _code;
    }
    //////////////////////////////////////////////////////////////////////////
    EHttpCode AppleHttpResponse::getCode() const
    {
        return m_code;
    }
    //////////////////////////////////////////////////////////////////////////
    void AppleHttpResponse::setError( const String & _error )
    {
        m_error = _error;
    }
    //////////////////////////////////////////////////////////////////////////
    const String & AppleHttpResponse::getError() const
    {
        return m_error;
    }
    //////////////////////////////////////////////////////////////////////////
    void AppleHttpResponse::setSuccessful( bool _successful )
    {
        m_successful = _successful;
    }
    //////////////////////////////////////////////////////////////////////////
    bool AppleHttpResponse::isSuccessful() const
    {
        return m_successful;
    }
    //////////////////////////////////////////////////////////////////////////
}