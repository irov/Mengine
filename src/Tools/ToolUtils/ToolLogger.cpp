    //////////////////////////////////////////////////////////////////////////
    ToolLogger::ToolLogger()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    ToolLogger::~ToolLogger()
    {
    }
    //////////////////////////////////////////////////////////////////////////
    void ToolLogger::_log( const LoggerRecordInterfacePtr & _record )
    {
        LoggerMessage message;
        _record->getMessage( &message );

        ::printf( "%.*s"
            , message.size
            , message.data
        );
    };
