#	include "XmlListener.h"

//////////////////////////////////////////////////////////////////////////
XmlListener::XmlListener()
	: m_reference(0)
{
}

//////////////////////////////////////////////////////////////////////////
XmlListener::~XmlListener()
{
}
//////////////////////////////////////////////////////////////////////////
void XmlListener::incref()
{
	++m_reference;
}
//////////////////////////////////////////////////////////////////////////
bool XmlListener::decref()
{
	return --m_reference == 0;
}