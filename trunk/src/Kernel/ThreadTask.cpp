#	include "ThreadTask.h"

namespace Menge
{
	//////////////////////////////////////////////////////////////////////////
	ThreadTask::ThreadTask()
		: m_finish(false)
		, m_complete(false)
		, m_successful(false)
		, m_cancel(false)
	{
	}
	//////////////////////////////////////////////////////////////////////////
	ThreadTask::~ThreadTask()
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void ThreadTask::main()
	{
		if( m_cancel == true )
		{
			m_successful = false;
			m_finish = true;

			return;
		}

		m_successful = this->_onMain();
		m_finish = true;
	}
	//////////////////////////////////////////////////////////////////////////
	bool ThreadTask::run()
	{
		bool state = this->_onRun();

		return state;
	}
	//////////////////////////////////////////////////////////////////////////
	bool ThreadTask::isComplete() const
	{
		return m_complete;
	}
	//////////////////////////////////////////////////////////////////////////
	bool ThreadTask::isCancel() const
	{
		return m_cancel;
	}
	//////////////////////////////////////////////////////////////////////////
	bool ThreadTask::_onMain()
	{
        return true;
	}
	//////////////////////////////////////////////////////////////////////////
	bool ThreadTask::_onRun()
	{
		return true;
	}
	//////////////////////////////////////////////////////////////////////////
	bool ThreadTask::cancel()
	{
		if( m_cancel == true ||
			m_finish == true ||
			m_complete == true )
		{
			return false;
		}

		m_cancel = true;

		this->_onCancel();

		return true;
	}
	//////////////////////////////////////////////////////////////////////////
	void ThreadTask::_onCancel()
	{
	}
	//////////////////////////////////////////////////////////////////////////
	bool ThreadTask::update()
	{
		if( m_cancel == true ||
			m_complete == true )
		{
			return true;
		}

		this->_onUpdate();

		if( m_finish == true )
		{
			m_complete = true;

			this->_onComplete( m_successful );
		}

		return m_complete;
	}
	//////////////////////////////////////////////////////////////////////////
	void ThreadTask::_onUpdate()
	{
	}
	//////////////////////////////////////////////////////////////////////////	
	void ThreadTask::_onComplete( bool _successful )
	{
		(void)_successful;
		//Empty
	}
}