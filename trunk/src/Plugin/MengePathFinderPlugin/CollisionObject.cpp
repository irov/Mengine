#	include "CollisionObject.h"

namespace Menge
{
	//////////////////////////////////////////////////////////////////////////
	CollisionObject::CollisionObject()
		: m_radius(0.f)
		, m_mass(0.f)
		, m_motor(nullptr)
		, m_dead(false)
		, m_ghost(false)
	{
	}
	//////////////////////////////////////////////////////////////////////////
	CollisionObject::~CollisionObject()
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void CollisionObject::setRadius( float _radius )
	{
		m_radius = _radius;
	}
	//////////////////////////////////////////////////////////////////////////
	float CollisionObject::getRadius() const
	{
		return m_radius;
	}
	//////////////////////////////////////////////////////////////////////////
	void CollisionObject::setMass( float _mass )
	{
		m_mass = _mass;
	}
	//////////////////////////////////////////////////////////////////////////
	float CollisionObject::getMass() const
	{
		return m_mass;
	}
	//////////////////////////////////////////////////////////////////////////
	void CollisionObject::setMotor( CollisionMotor * _motor )
	{
		m_motor = _motor;
	}
	//////////////////////////////////////////////////////////////////////////
	CollisionMotor * CollisionObject::getMotor() const
	{
		return m_motor;
	}
	//////////////////////////////////////////////////////////////////////////
	void CollisionObject::setDead( bool _dead )
	{
		m_dead = _dead;
	}
	//////////////////////////////////////////////////////////////////////////
	bool CollisionObject::getDead() const
	{
		return m_dead;
	}
	//////////////////////////////////////////////////////////////////////////
	void CollisionObject::setGhost( bool _ghost )
	{
		m_ghost = _ghost;
	}
	//////////////////////////////////////////////////////////////////////////
	bool CollisionObject::getGhost() const
	{
		return m_ghost;
	}
	//////////////////////////////////////////////////////////////////////////
	void CollisionObject::update( float _timing )
	{
		if( m_motor == nullptr )
		{
			return;
		}

		if( m_dead == true )
		{
			return;
		}

		m_motor->update( _timing, this );
	}
}