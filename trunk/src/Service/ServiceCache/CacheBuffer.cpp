#	include "CacheBuffer.h"

namespace Menge
{
	//////////////////////////////////////////////////////////////////////////
	CacheBuffer::CacheBuffer()
		: m_memory(nullptr)
	{
	}
	//////////////////////////////////////////////////////////////////////////
	CacheBuffer::~CacheBuffer()
	{
	}
	//////////////////////////////////////////////////////////////////////////	
	void CacheBuffer::setBuffer( CacheBufferMemory * _memory )
	{
		m_memory = _memory;
	}
	//////////////////////////////////////////////////////////////////////////
	TBlobject::value_type * CacheBuffer::getMemory() const
	{
		TBlobject::value_type * buffer = m_memory->buffer;

		return buffer;
	}
	//////////////////////////////////////////////////////////////////////////
	void CacheBuffer::_destroy()
	{
		m_memory->lock = false;
	}
	//////////////////////////////////////////////////////////////////////////
}